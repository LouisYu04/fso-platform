# TestScintillationIndexPlaneWave — 平面波闪烁指数（全范围）测试
#
# 测试目的:
#   验证 scintillation_index_plane_wave() 在全部湍流强度范围内的
#   数值正确性和物理合理性。
#
# 测试原理:
#   平面波在全湍流范围内的闪烁指数使用修正模型（如 Andrews 模型），
#   覆盖弱湍流到饱和湍流的完整过渡:
#     - 弱湍流 (σ_R² << 1): σ_I² ≈ σ_R²（与弱近似一致）
#     - 中强湍流: 闪烁指数增长减缓，开始饱和
#     - 饱和湍流 (σ_R² → ∞): σ_I² → 1（闪烁饱和）
#   物理上，闪烁饱和是因为光波在多散射过程中空间相干性逐渐丧失，
