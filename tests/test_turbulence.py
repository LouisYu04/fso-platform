    # -------------------------------------------------------------------
    # 测试目的: 验证对负束腰的报错
    # 测试原理: 束腰半径必须为正数。
    # 预期行为: 抛出 ValueError
    # -------------------------------------------------------------------
    def test_value_error_negative_waist(self):
        with pytest.raises(ValueError, match="W0_m"):
            long_term_beam_size(-0.01, 1.55e-6, 1000, 0.1)

