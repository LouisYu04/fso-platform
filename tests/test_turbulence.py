# TestFriedParameter — Fried 参数（大气相干长度）测试
#
# 测试目的:
#   验证 fried_parameter() 函数计算的 r₀ 值的正确性。
#
# 测试原理:
#   Fried 参数 r₀（大气相干长度）是描述大气湍流对光学系统影响
#   的核心参数，其物理意义为: 在 r₀ 尺度内，波前相位差均方根
#   约为 1 弧度。对于平面波:
#     r₀ = [0.423 · k² · Cn² · L]^(-3/5)
#   其中 k = 2π/λ 为波数。
#   关键物理特性:
#     - r₀ ∝ Cn²^(-3/5): 湍流越强，相干长度越小
#     - r₀ ∝ λ^(6/5): 长波长相干性更好
#     - r₀ ∝ L^(-3/5): 距离越长，相干性越差
#   r₀ 直接影响: 望远镜分辨率极限、自适应光学系统设计、光斑尺寸。
#   典型值: 可见光波段 r₀ ≈ 5-20cm（天文观测站），
#   近红外波段 r₀ 更大。
#
# 预期行为:
#   - 典型输入下 r₀ 在 1cm-50cm 量级
#   - 弱湍流产生的 r₀ 大于强湍流
#   - 长波长产生的 r₀ 大于短波长
#   - 非法输入正确报错
# ============================================================================
