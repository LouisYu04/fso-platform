# TestCn2Typical — 典型 Cn² 参考值测试
#
# 测试目的:
#   验证 cn2_typical() 函数返回的标准湍流强度参考值。
#
# 测试原理:
#   折射率结构常数 Cn² 是表征大气光学湍流强度的基本参数，
#   取值范围通常跨越多个数量级:
#     - 弱湍流 (weak):       Cn² ≈ 1×10^(-16)  m^(-2/3)
#     - 中等湍流 (moderate):  Cn² ≈ 1×10^(-14)  m^(-2/3)
#     - 强湍流 (strong):     Cn² ≈ 1×10^(-13)  m^(-2/3)
#     - 极强湍流 (very_strong): Cn² ≈ 1×10^(-12) m^(-2/3)
#   这些典型值广泛应用于 FSO 链路分析和系统设计中，是链路预算
#   计算的基准参考。实际值受海拔、昼夜、季节、地理位置等因素影响。
#
# 预期行为:
#   - 已知条件名称返回对应的标准 Cn² 值
#   - 未知条件名称触发警告并返回默认值（中等湍流 1e-14）
#   - 各强度等级的值严格单调递增
# ============================================================================
class TestCn2Typical:
    """Tests for cn2_typical() — typical Cn2 reference values."""

    # -------------------------------------------------------------------
    # 测试目的: 验证标准湍流强度名称对应的 Cn² 值
    # 测试原理: 验证四个预设条件名称返回正确的标准参考值。
    # 预期行为:
    #   - "weak" → 1e-16
    #   - "moderate" → 1e-14
    #   - "strong" → 1e-13
    #   - "very_strong" → 1e-12
    # -------------------------------------------------------------------
    def test_known_conditions(self):
        assert cn2_typical("weak") == pytest.approx(1e-16)
        assert cn2_typical("moderate") == pytest.approx(1e-14)
        assert cn2_typical("strong") == pytest.approx(1e-13)
        assert cn2_typical("very_strong") == pytest.approx(1e-12)

    # -------------------------------------------------------------------
    # 测试目的: 验证未知条件名称触发警告
    # 测试原理: 当传入未定义的条件名称时，函数应警告用户并
    #           返回默认值（中等湍流）。
    # 预期行为: 触发 UserWarning，警告信息包含 "未知 condition"
    # -------------------------------------------------------------------
    def test_unknown_condition_warns(self):
        with pytest.warns(UserWarning, match="未知 condition"):
            result = cn2_typical("undefined_condition")
        assert result == pytest.approx(1e-14)

    # -------------------------------------------------------------------
    # 测试目的: 验证未知条件返回默认值
    # 测试原理: 除警告外，函数应返回安全默认值以避免程序崩溃。
    #           默认值通常取中等湍流条件。
    # 预期行为: 未知条件名称返回 1e-14
    # -------------------------------------------------------------------
    def test_unknown_condition_returns_default(self):
        with pytest.warns(UserWarning):
            result = cn2_typical("nonexistent")
        assert result == pytest.approx(1e-14)

    # -------------------------------------------------------------------
    # 测试目的: 验证各强度等级 Cn² 值严格递增
    # 测试原理: weak < moderate < strong < very_strong
    #           物理上，湍流强度等级顺序应严格保持单调性。
    # 预期行为: 相邻等级的值严格递增（每个后续值 > 前一个值）
    # -------------------------------------------------------------------
    def test_monotonic_values(self):
        vals = [
            cn2_typical("weak"),
            cn2_typical("moderate"),
            cn2_typical("strong"),
            cn2_typical("very_strong"),
        ]
        for i in range(len(vals) - 1):
            assert vals[i + 1] > vals[i]
