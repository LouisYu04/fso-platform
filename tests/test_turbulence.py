    # 测试目的: 验证典型参数下 r₀ 在合理物理范围内
    # 测试原理: 对中等湍流 (Cn²=1e-14)、1550nm、1km 链路，
    #           大气相干长度通常在厘米到分米量级。
    # 预期行为: 0.01m < r₀ < 0.5m
    # -------------------------------------------------------------------
    def test_typical_value(self):
        r0 = fried_parameter(1e-14, 1.55e-6, 1000)
        assert 0.01 < r0 < 0.5

    # -------------------------------------------------------------------
    # 测试目的: 验证 r₀ 随湍流强度增大而减小
