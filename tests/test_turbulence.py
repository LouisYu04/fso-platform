    # 测试原理: 波长不能为零。
    # 预期行为: 抛出 ValueError
    # -------------------------------------------------------------------
    def test_value_error_zero_wavelength(self):
        with pytest.raises(ValueError, match="wavelength_m"):
            long_term_beam_size(0.01, 0.0, 1000, 0.1)

    # -------------------------------------------------------------------
    # 测试目的: 验证对负 Rytov 方差的报错
    # 测试原理: σ_R² 不能为负。
    # 预期行为: 抛出 ValueError
