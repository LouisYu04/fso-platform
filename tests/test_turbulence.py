# TestScintillationIndexWeak — 弱湍流闪烁指数测试
#
# 测试目的:
#   验证 scintillation_index_weak() 函数在弱湍流条件下的闪烁指数计算。
#
# 测试原理:
#   在弱湍流条件下（σ_R² < 1），一阶 Rytov 近似给出:
#     σ_I² ≈ σ_R²
#   即闪烁指数（强度归一化方差）近似等于 Rytov 方差。
#   这是弱湍流理论的基本结果——闪烁的强度直接由湍流方差决定。
#   当 σ_R² ≥ 1 时，Rytov 近似不再有效，函数应发出警告。
#
# 预期行为:
#   - 输入值在 [0, 1) 范围内直接返回原值（恒等映射）
#   - 输入值 ≥ 1 时触发 UserWarning
#   - 负值抛出 ValueError
# ============================================================================
class TestScintillationIndexWeak:
    """Tests for scintillation_index_weak() — weak turbulence scintillation."""

    # -------------------------------------------------------------------
    # 测试目的: 验证弱湍流下的恒等映射 σ_I² = σ_R²
    # 测试原理: 弱湍流近似下，闪烁指数直接等于 Rytov 方差。
    #           此测试验证该映射的数值正确性。
    # 预期行为: scintillation_index_weak(0.5) ≈ 0.5
    # -------------------------------------------------------------------
    def test_identity_mapping(self):
        assert scintillation_index_weak(0.5) == pytest.approx(0.5)

    # -------------------------------------------------------------------
    # 测试目的: 验证对负输入的报错
    # 测试原理: Rytov 方差不能为负，闪烁指数同样不能接受负输入。
    # 预期行为: 抛出 ValueError，错误信息包含 "sigma_R2"
    # -------------------------------------------------------------------
    def test_value_error_negative(self):
        with pytest.raises(ValueError, match="sigma_R2"):
            scintillation_index_weak(-0.1)

    # -------------------------------------------------------------------
    # 测试目的: 验证在 σ_R² > 1 时的警告提示
    # 测试原理: 当 σ_R² ≥ 1 时，弱湍流近似不再有效，函数应警告用户
    #           结果可能不可靠。
    # 预期行为: 触发 UserWarning，警告信息包含 "弱湍流近似"
    # -------------------------------------------------------------------
    def test_warning_when_above_1(self):
        with pytest.warns(UserWarning, match="弱湍流近似"):
            scintillation_index_weak(1.5)

    # -------------------------------------------------------------------
    # 测试目的: 验证在 σ_R² < 1 时不触发警告
    # 测试原理: 当输入在弱湍流有效范围内时，不应产生任何警告。
    #           使用 catch_warnings 将过滤器设为 "error"，
    #           如有警告会被转换为异常。
    # 预期行为: 无警告抛出，返回正确值
    # -------------------------------------------------------------------
    def test_no_warning_when_below_1(self):
        with warnings.catch_warnings():
            warnings.simplefilter("error")
            result = scintillation_index_weak(0.5)
        assert result == pytest.approx(0.5)

    # -------------------------------------------------------------------
    # 测试目的: 验证向量化输入下的警告行为
    # 测试原理: 当输入数组中包含超过弱湍流范围的元素时（如 1.5），
    #           应对整个数组操作发出警告，即使部分元素仍在范围内。
    # 预期行为: 触发 UserWarning，警告信息包含 "弱湍流近似"
    # -------------------------------------------------------------------
    def test_vectorized_warning(self):
        with pytest.warns(UserWarning, match="弱湍流近似"):
            scintillation_index_weak(np.array([0.5, 1.5, 0.3]))


