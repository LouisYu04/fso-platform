# TestRytovVarianceSpherical — 球面波 Rytov 方差测试
#
# 测试目的:
#   验证 rytov_variance_spherical() 函数在球面波假设下的正确性。
#
# 测试原理:
#   球面波 Rytov 方差与平面波 Rytov 方差的关系为:
#     β_R² = 0.4 · σ_R²
#   其中 σ_R² 为平面波 Rytov 方差，β_R² 为球面波 Rytov 方差。
#   系数 0.4 源于球面波在 Kolmogorov 湍流中的传播几何特性——
#   球面波前在传播过程中不断扩展，累积的湍流效应小于平面波。
#   物理上，对于点光源发出的球面波，湍流引入的相位扰动在横向
#   部分相干抵消，因此方差小于同等条件下的平面波。
#
# 预期行为:
#   球面波方差恒为平面波方差的 0.4 倍；零距离时方差为零；
#   输入验证错误应转发至底层函数。
# ============================================================================
class TestRytovVarianceSpherical:
    """Tests for rytov_variance_spherical() — spherical wave variance."""

    # -------------------------------------------------------------------
    # 测试目的: 验证球面波与平面波 Rytov 方差的 0.4 比例关系
    # 测试原理: 理论上 β_R² = 0.4 · σ_R²。此测试对比同一组参数
    #           下两个函数的返回值，验证比例系数是否为精确的 0.4。
    # 预期行为: β_R² ≈ 0.4 × σ_R²，相对误差 ≤ 1e-10
    # -------------------------------------------------------------------
    def test_ratio_is_0_4(self):
        sigma2 = rytov_variance(1e-14, 1.55e-6, 1000)
        beta2 = rytov_variance_spherical(1e-14, 1.55e-6, 1000)
        assert beta2 == pytest.approx(0.4 * sigma2, rel=1e-10)

    # -------------------------------------------------------------------
    # 测试目的: 验证零距离时球面波方差为零
    # 测试原理: 与平面波相同，传输距离为零时无湍流效应，方差应为零。
    # 预期行为: β_R² = 0.0
    # -------------------------------------------------------------------
    def test_zero_distance(self):
        beta2 = rytov_variance_spherical(1e-14, 1.55e-6, 0.0)
        assert beta2 == pytest.approx(0.0)

    # -------------------------------------------------------------------
    # 测试目的: 验证球面波函数正确传播输入验证错误
    # 测试原理: 球面波函数内部可能调用平面波函数或共享验证逻辑。
    #           非法输入（如 Cn² = 0）应同样导致 ValueError。
    # 预期行为: 抛出 ValueError（不限定具体错误信息）
    # -------------------------------------------------------------------
    def test_propagates_errors(self):
        with pytest.raises(ValueError):
            rytov_variance_spherical(0, 1.55e-6, 1000)


