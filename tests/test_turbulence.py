# ============================================================================
# TestRytovVariance — 平面波 Rytov 方差测试
#
# 测试目的:
#   验证 rytov_variance() 函数在平面波假设下 Rytov 方差 σ_R² 的正确性。
#   Rytov 方差是表征大气湍流强度的核心无量纲参数，用于判断湍流强弱。
#
# 测试原理:
#   平面波 Rytov 方差的理论公式为:
#     σ_R² = 1.23 · Cn² · k^(7/6) · L^(11/6)
#   其中:
#     - Cn²: 折射率结构常数 [m^(-2/3)]，表征湍流强度
#     - k = 2π/λ: 波数 [m^(-1)]，λ 为光波长
#     - L: 传输距离 [m]
#   该公式基于 Kolmogorov 湍流谱的一阶 Rytov 近似，适用于弱湍流条件。
#   关键物理规律:
#     - σ_R² ∝ Cn²: 湍流越强，方差越大
#     - σ_R² ∝ L^(11/6): 随距离超线性增长
#     - σ_R² ∝ λ^(-7/6): 长波长受湍流影响更小
#     - L = 0 时 σ_R² = 0: 无传输路径则无闪烁
#
# 预期行为:
#   所有测试应通过，验证函数在不同输入下的数值正确性、单调性、
#   边界条件和向量化支持。
# ============================================================================
class TestRytovVariance:
    """Tests for rytov_variance() — plane wave Rytov variance."""

    # -------------------------------------------------------------------
    # 测试目的: 验证典型输入参数下 Rytov 方差为正值
    # 测试原理: 给定中等湍流 (Cn²=1e-14)、典型近红外波长 (1550nm)
    #           和 1km 传输距离，Rytov 方差必须为正数。
    #           这是最基本的合理性检查。
    # 预期行为: 返回值 σ_R² > 0
    # -------------------------------------------------------------------
    def test_typical_value(self):
        Cn2, wl, d = 1e-14, 1.55e-6, 1000
        sigma2 = rytov_variance(Cn2, wl, d)
        assert sigma2 > 0

    # -------------------------------------------------------------------
    # 测试目的: 验证传输距离为零时 Rytov 方差为零
    # 测试原理: 当 L = 0 时，光波未经历湍流介质，闪烁方差应为零。
    #           这是物理边界条件测试。
    # 预期行为: σ_R² = 0.0（浮点近似相等）
    # -------------------------------------------------------------------
    def test_zero_distance(self):
        sigma2 = rytov_variance(1e-14, 1.55e-6, 0.0)
        assert sigma2 == pytest.approx(0.0)

    # -------------------------------------------------------------------
    # 测试目的: 验证向量化输入中距离全为零时的行为
    # 测试原理: 当函数接受 NumPy 数组作为 distance_m 参数时，
    #           应对每个元素独立计算。全零数组应返回全零结果。
    # 预期行为: 返回与输入形状相同的数组，所有元素为零
    # -------------------------------------------------------------------
    def test_zero_distance_array(self):
        sigma2 = rytov_variance(1e-14, 1.55e-6, np.array([0.0, 0.0, 0.0]))
        assert np.all(sigma2 == 0.0)

    # -------------------------------------------------------------------
    # 测试目的: 验证 Rytov 方差随传输距离单调递增
    # 测试原理: 根据公式 σ_R² ∝ L^(11/6)，方差应随距离超线性增长。
    #           500m 处的值应小于 1000m 处的值。
    # 预期行为: σ_R²(1000m) > σ_R²(500m)
    # -------------------------------------------------------------------
    def test_increases_with_distance(self):
        Cn2, wl = 1e-14, 1.55e-6
        s_500 = rytov_variance(Cn2, wl, 500.0)
        s_1000 = rytov_variance(Cn2, wl, 1000.0)
        assert s_1000 > s_500

    # -------------------------------------------------------------------
    # 测试目的: 验证 Rytov 方差随湍流强度单调递增
    # 测试原理: σ_R² ∝ Cn²，折射率结构常数越大，湍流越强，方差越大。
    #           比较弱湍流 (1e-15) 和强湍流 (1e-13) 下的结果。
    # 预期行为: σ_R²(强湍流) > σ_R²(弱湍流)
    # -------------------------------------------------------------------
    def test_increases_with_cn2(self):
        wl, d = 1.55e-6, 1000
        s_weak = rytov_variance(1e-15, wl, d)
        s_strong = rytov_variance(1e-13, wl, d)
        assert s_strong > s_weak

    # -------------------------------------------------------------------
    # 测试目的: 验证 Rytov 方差与波长的反比关系
    # 测试原理: σ_R² ∝ λ^(-7/6)，因此较长波长受湍流影响较小。
    #           1550nm 的方差应小于 850nm 的方差。
    #           这是 FSO 系统常使用 1550nm 而非 850nm 的物理依据之一。
    # 预期行为: σ_R²(1550nm) < σ_R²(850nm)
    # -------------------------------------------------------------------
    def test_scales_with_wavelength(self):
        Cn2, d = 1e-14, 1000
        s_850 = rytov_variance(Cn2, 850e-9, d)
        s_1550 = rytov_variance(Cn2, 1550e-9, d)
        assert s_1550 < s_850

    # -------------------------------------------------------------------
    # 测试目的: 验证对非法 Cn² 输入的正确报错
    # 测试原理: 折射率结构常数 Cn² 必须为正数（物理上，Cn² ≤ 0 无意义）。
    #           零值表示无湍流（不适用于 Rytov 近似），负值物理上不可能。
    # 预期行为: 对 Cn² = 0 和 Cn² = -1e-14 均抛出 ValueError，
    #           错误信息包含 "Cn2"
    # -------------------------------------------------------------------
    @pytest.mark.parametrize("Cn2", [0, -1e-14])
    def test_value_error_negative_cn2(self, Cn2):
        with pytest.raises(ValueError, match="Cn2"):
            rytov_variance(Cn2, 1.55e-6, 1000)

    # -------------------------------------------------------------------
    # 测试目的: 验证对零波长的报错
    # 测试原理: 波数 k = 2π/λ，λ = 0 时分母为零，公式无定义。
    #           物理上不可能存在零波长的光波。
    # 预期行为: 抛出 ValueError，错误信息包含 "wavelength_m"
    # -------------------------------------------------------------------
    def test_value_error_zero_wavelength(self):
        with pytest.raises(ValueError, match="wavelength_m"):
            rytov_variance(1e-14, 0.0, 1000)

    # -------------------------------------------------------------------
    # 测试目的: 验证对负波长的报错
    # 测试原理: 光波长必须为正数，负波长在物理上无意义。
    # 预期行为: 抛出 ValueError，错误信息包含 "wavelength_m"
    # -------------------------------------------------------------------
    def test_value_error_negative_wavelength(self):
        with pytest.raises(ValueError, match="wavelength_m"):
            rytov_variance(1e-14, -1e-6, 1000)

    # -------------------------------------------------------------------
    # 测试目的: 验证对负传输距离的报错
    # 测试原理: 传输距离 L 必须为非负数，负距离在物理上无意义。
    # 预期行为: 抛出 ValueError，错误信息包含 "distance_m"
    # -------------------------------------------------------------------
    def test_value_error_negative_distance(self):
        with pytest.raises(ValueError, match="distance_m"):
            rytov_variance(1e-14, 1.55e-6, -100.0)

    # -------------------------------------------------------------------
    # 测试目的: 验证距离参数的向量化支持
    # 测试原理: 函数应支持 NumPy 数组作为 distance_m 输入，对每个元
    #           素独立计算后返回同形状数组。同时验证单调性在向量化
    #           结果中仍然保持。
    # 预期行为:
    #   - 返回形状为 (4,) 的数组
    #   - 结果随距离严格单调递增
    # -------------------------------------------------------------------
    def test_vectorized_distance(self):
        Cn2, wl = 1e-14, 1.55e-6
        distances = np.array([100, 500, 1000, 2000])
        result = rytov_variance(Cn2, wl, distances)
        assert result.shape == (4,)
        for i in range(len(distances) - 1):
            assert result[i + 1] > result[i]

    # -------------------------------------------------------------------
    # 测试目的: 验证向量化输入中混合零距离和非零距离的正确性
    # 测试原理: 当距离数组包含零和非零值时，零距离对应的结果应为零，
    #           非零距离应保持单调递增关系。
    # 预期行为:
    #   - 第一个元素 (L=0) 为 0.0
    #   - 后续元素为正且递增
    # -------------------------------------------------------------------
    def test_vectorized_with_mixed_zero(self):
        Cn2, wl = 1e-14, 1.55e-6
        distances = np.array([0.0, 500.0, 1000.0])
        result = rytov_variance(Cn2, wl, distances)
        assert result[0] == 0.0
        assert result[1] > 0
        assert result[2] > result[1]


