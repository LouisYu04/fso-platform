# TestLongTermBeamSize — 长期光束尺寸测试
#
# 测试目的:
#   验证 long_term_beam_size() 函数计算湍流展宽后的光束半径正确性。
#
# 测试原理:
#   长期光束尺寸 W_LT 描述高斯光束经大气湍流传输后的有效半径:
#     W_LT² = W² · (1 + T)
#   其中 W 为自由空间光束半径（由高斯光学决定），T 为湍流展宽项。
#   自由空间中的高斯光束传播:
#     W(z) = W₀ · √(1 + (z/z_R)²)
#   其中 z_R = π·W₀²/λ 为瑞利距离。
#   湍流展宽效应使光束有效半径大于自由空间衍射极限，这是 FSO
#   系统中重要的损耗来源——展宽越大，接收端收集到的功率越少。
#
# 预期行为:
#   - 无湍流 (σ_R²=0) 时退化为自由空间高斯光束传播公式
#   - 有湍流时结果大于自由空间值
#   - 零距离时光束半径等于束腰半径
#   - 非法输入正确报错
#   - 支持向量化
# ============================================================================
class TestLongTermBeamSize:
    """Tests for long_term_beam_size() — turbulence-broadened beam."""

    # -------------------------------------------------------------------
    # 测试目的: 验证无湍流时退化为自由空间衍射公式
    # 测试原理: 当 σ_R² = 0 时，湍流展宽项为零，光束尺寸应由
    #           标准高斯光束公式 W(z) = W₀·√(1 + (z/z_R)²) 给出。
    #           瑞利距离 z_R = π·W₀²/λ。
    # 预期行为: 函数返回值与自由空间公式计算结果一致 (rel ≤ 1e-6)
    # -------------------------------------------------------------------
    def test_free_space_limit(self):
        W0, wl, d = 0.01, 1.55e-6, 1000
        W_lt = long_term_beam_size(W0, wl, d, 0.0)
        z_R = np.pi * W0**2 / wl
        W_free = W0 * np.sqrt(1 + (d / z_R)**2)
        assert W_lt == pytest.approx(W_free, rel=1e-6)

    # -------------------------------------------------------------------
    # 测试目的: 验证湍流使光束展宽
    # 测试原理: 存在湍流时 (σ_R² > 0)，光束尺寸应大于自由空间值。
    #           这是湍流导致光束展宽的直接物理验证。
    # 预期行为: W_turb(σ_R²=0.5) > W_free(σ_R²=0)
    # -------------------------------------------------------------------
    def test_turbulence_broadens_beam(self):
        W0, wl, d = 0.01, 1.55e-6, 1000
        W_free = long_term_beam_size(W0, wl, d, 0.0)
        W_turb = long_term_beam_size(W0, wl, d, 0.5)
        assert W_turb > W_free

    # -------------------------------------------------------------------
    # 测试目的: 验证零传输距离时光束半径等于束腰
    # 测试原理: 当 z = 0 时，高斯光束半径即为束腰半径 W₀，
    #           不受衍射或湍流影响。
    # 预期行为: long_term_beam_size(W₀, ..., 0, ...) ≈ W₀
    # -------------------------------------------------------------------
    def test_distance_zero(self):
        W = long_term_beam_size(0.01, 1.55e-6, 0.0, 0.1)
        assert W == pytest.approx(0.01)

    # -------------------------------------------------------------------
    # 测试目的: 验证对零束腰的报错
    # 测试原理: 束腰 W₀ = 0 意味着无光束，物理上无意义。
