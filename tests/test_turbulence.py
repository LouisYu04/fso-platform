# TestTurbulenceRegime — 湍流强度分类测试
#
# 测试目的:
#   验证 turbulence_regime() 函数根据 Rytov 方差 σ_R² 对湍流强度
#   进行正确分类。
#
# 测试原理:
#   基于经典湍流理论，Rytov 方差将湍流分为三个区间:
#     - 弱湍流 (Weak):      σ_R² < 1.0
#       闪烁指数 ≈ σ_R²，Rytov 近似有效
#     - 中强湍流 (Moderate): 1.0 ≤ σ_R² < 25.0
#       闪烁指数开始饱和，需使用更复杂的闪烁模型
#     - 饱和湍流 (Strong/Saturation): σ_R² ≥ 25.0
#       闪烁达到饱和，闪烁指数趋近于 1
#   边界值: σ_R² = 1.0 为弱→中强分界，σ_R² = 25.0 为中强→饱和分界。
#
# 预期行为:
#   各区间内任意值返回对应的类别字符串，边界值按约定归属。
#   负值应抛出 ValueError。
# ============================================================================
class TestTurbulenceRegime:
    """Tests for turbulence_regime() — turbulence strength classification."""

    # -------------------------------------------------------------------
    # 测试目的: 验证各湍流区间分类的正确性
    # 测试原理: 使用参数化测试覆盖三个区间的典型值:
    #   - [0, 0.99]: 弱湍流
    #   - [1.0, 25.0]: 中强湍流
    #   - [25.01, 100]: 饱和湍流
    # 预期行为: 返回值与预期的中文类别字符串一致
    # -------------------------------------------------------------------
    @pytest.mark.parametrize("sigma_R2, expected", [
        (0.0, "弱湍流"),
        (0.1, "弱湍流"),
        (0.5, "弱湍流"),
        (0.99, "弱湍流"),
        (1.0, "中强湍流"),
        (5.0, "中强湍流"),
        (10.0, "中强湍流"),
        (25.0, "中强湍流"),
        (25.01, "饱和湍流"),
        (50.0, "饱和湍流"),
        (100.0, "饱和湍流"),
    ])
    def test_regime_classification(self, sigma_R2, expected):
        assert turbulence_regime(sigma_R2) == expected

    # -------------------------------------------------------------------
    # 测试目的: 验证负 Rytov 方差输入的正确报错
    # 测试原理: σ_R² 为平方量，物理上不可能为负值。
    # 预期行为: 对任意负值抛出 ValueError，错误信息包含 "sigma_R2"
    # -------------------------------------------------------------------
    @pytest.mark.parametrize("sigma_R2", [-0.1, -1.0, -100.0])
    def test_value_error_negative(self, sigma_R2):
        with pytest.raises(ValueError, match="sigma_R2"):
            turbulence_regime(sigma_R2)

    # -------------------------------------------------------------------
    # 测试目的: 精确验证弱→中强湍流边界 (σ_R² = 1.0)
    # 测试原理: 边界判定应满足: σ_R² < 1.0 为弱湍流，
    #           σ_R² ≥ 1.0 为中强湍流。
    # 预期行为: 0.999 → "弱湍流", 1.0 → "中强湍流"
    # -------------------------------------------------------------------
    def test_boundary_weak_to_moderate(self):
        assert turbulence_regime(0.999) == "弱湍流"
        assert turbulence_regime(1.0) == "中强湍流"

    # -------------------------------------------------------------------
    # 测试目的: 精确验证中强→饱和湍流边界 (σ_R² = 25.0)
    # 测试原理: 边界判定应满足: σ_R² < 25.0 为中强湍流，
    #           σ_R² ≥ 25.0 为饱和湍流。
    # 预期行为: 25.0 → "中强湍流", 25.001 → "饱和湍流"
    # -------------------------------------------------------------------
    def test_boundary_moderate_to_strong(self):
        assert turbulence_regime(25.0) == "中强湍流"
        assert turbulence_regime(25.001) == "饱和湍流"


